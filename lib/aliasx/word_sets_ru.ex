  @all_words @simple ++ @easy ++ @medium ++ @difficult

  @doc """
  Возвращает набор слов для указанного уровня сложности.
  """
  def get_words(:simple), do: @simple
  def get_words(:easy), do: @easy
  def get_words(:medium), do: @medium
  def get_words(:difficult), do: @difficult
  def get_words(:all), do: @all_words
  # Значение по умолчанию
  def get_words(_), do: @medium

  @doc """
  Возвращает карту всех наборов слов по уровням сложности.
  """
  def all_word_sets do
    %{
      simple: @simple,
      easy: @easy,
      medium: @medium,
      difficult: @difficult
    }
  end

  @doc """
  Возвращает случайный выбор слов указанной сложности.
  """
  def random_words(difficulty, count \\ 100) do
    difficulty
    |> get_words()
    |> Enum.shuffle()
    |> Enum.take(count)
  end

  @doc """
  Возвращает количество слов для уровня сложности.
  """
  def word_count(difficulty) do
    difficulty
    |> get_words()
    |> length()
  end
